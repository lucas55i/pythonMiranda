"""
Python = Linguagem de programação
Tipo de tipagem = Dinamica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
# Aspas simples
print('Lucas -')
print('Lucas "Silva"') # Aspas duplas dentro de aspas simples

# Aspas Duplas
print("Lucas --")

# Escape
print("Lucas \"Silva\"")

# r
print(r"Lucas \"Silva\"")
